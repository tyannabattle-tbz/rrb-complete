/**
 * Helper functions for channel management
 */

export function getChannelById(id: string): Channel | undefined {
  return CHANNELS.find(ch => ch.id === id);
}

export function getChannelsByCategory(category: Channel['category']): Channel[] {
  return CHANNELS.filter(ch => ch.category === category);
}

export function getChannelsByGenre(genre: string): Channel[] {
  return CHANNELS.filter(ch => ch.genre.toLowerCase().includes(genre.toLowerCase()));
}

export function getPrimaryStreamUrl(channelId: string): string | null {
  const channel = getChannelById(channelId);
  return channel?.streamUrl || null;
}

export function getChannelStreams(channelId: string): { primary: string; fallback: string } | null {
  const channel = getChannelById(channelId);
  if (!channel) return null;
  return {
    primary: channel.streamUrl,
    fallback: channel.fallbackUrl,
  };
}

export function getAllChannels(): Channel[] {
  return CHANNELS;
}

export function getChannelCount(): number {
  return CHANNELS.length;
}

export function getChannelsByBitrate(bitrate: '128' | '320' | 'lossless'): Channel[] {
  return CHANNELS.filter(ch => ch.bitrate === bitrate);
}
