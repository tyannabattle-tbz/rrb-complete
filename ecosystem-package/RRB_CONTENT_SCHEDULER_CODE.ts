export interface ScheduleSlot {
  id: string;
  channelId: string;
  startTime: number;
  endTime: number;
  contentType: ContentType;
  contentId: string;
  title: string;
  artist?: string;
  genre?: string;
  frequency?: number;
  priority: 'normal' | 'high' | 'emergency';
  autoGenerated: boolean;
  status: 'scheduled' | 'playing' | 'completed' | 'skipped' | 'interrupted';
}

export type ContentType = 
  | 'music' | 'podcast' | 'commercial' | 'announcement'
  | 'healing_frequency' | 'audiobook' | 'live_broadcast'
  | 'emergency_alert' | 'station_id' | 'dj_segment';

export interface Channel {
  id: string;
  name: string;
  genre: string;
  frequency: number;
  isActive: boolean;
  currentSlot?: ScheduleSlot;
  nextSlot?: ScheduleSlot;
  listeners: number;
}

export interface ScheduleConfig {
  defaultFrequency: number;
  commercialBreakInterval: number; // minutes
  commercialBreakDuration: number; // seconds
  stationIdInterval: number; // minutes
  healingFrequencyRotation: boolean;
  emergencyOverride: boolean;
  autoFillGaps: boolean;
  maxConsecutiveSameArtist: number;
}

